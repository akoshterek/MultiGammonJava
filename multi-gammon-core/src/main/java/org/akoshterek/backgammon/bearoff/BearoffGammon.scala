  def getRaceBGprobs(board: Array[Int]): Vector[Long] = {
    require(board(5) == 0)
    var tot = 0
    var group = 0

    for (i <- 4 to 0 by -1) {
      if (board(i) != 0) {
        group += (0x1 << i)
      }
      tot += board(i)
    }

    require(group < 32)

    if (tot > 6) {
      Vector.empty[Long]
    } else {
      var grpSize = 0
      val b1 = Array.ofDim[Int](6)

     for (k <- 0 until 5)  {
        if ((group & (0x1 << k)) != 0) {
          require(board(k) > 0)
          b1(grpSize) = board(k) - 1
          grpSize += 1
        }
      }

      y(group - 1)(PositionId.positionIndex(grpSize, b1))
    }
  }
